% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
ClearUI(hObject, eventdata, handles);
[filename, pathname] = uigetfile({'*.jpg';'*.png';'*.bmp';'*.*'},'File Selector');
if isequal(filename,0)
   disp('User selected Cancel')
else
   disp(['User selected ', fullfile(pathname, filename)])
%     Filename=fullfile(pathname,filename);
    var=strcat(pathname,filename);
%     ORI_IMG=imread(var);
    [name,pref,prefCount,partyCount] = balletPaper(pathname,filename, handles);
    
    if(partyCount>1)
        errordlg('Voted for more than one party','Invalid Vote');
    elseif(prefCount>3)
        errordlg('Voted for more than three preferences','Invalid Vote');
    else
    N= strsplit(name,'.');
    set(handles.edit1,'string',N(1));
    C = strsplit(pref,',');
    set(handles.edit2,'string',C(1));
    set(handles.edit3,'string',C(2));
    set(handles.edit4,'string',C(3));
    end  
end
